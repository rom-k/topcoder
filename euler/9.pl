for ($a = 1; $a < 1000; $a++) {
    for ($b = $a; $b < 1000 - $a; $b++) {
        $c = 1000 - $a - $b;
        if ($a**2 + $b**2 == $c**2) {
            print "$a * $b * $c => ".$a*$b*$c."\n";
        }
    }
}

